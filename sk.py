# Function to add two 10-digit numbers
def add_10_digit_numbers(num1, num2):
    return num1 + num2

# Example usage
num1 = 1234567890  # Example 10-digit number
num2 = 9876543210  # Example 10-digit number

result = add_10_digit_numbers(num1, num2)
print(f"The sum of {num1} and {num2} is {result}")
